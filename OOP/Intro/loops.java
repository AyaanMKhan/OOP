package Intro;
public class loops {
    
    public static void main(String[] args) {

        int count = 0; // Initialize

        while(count < 5){ //Check

            System.out.println(count);
            count ++; // Update

        }    
        

        for(int i = 0; i < 5; i++){
            System.out.println(i);
        }

    }

}
